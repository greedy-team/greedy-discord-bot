    public int getSize() {
        return DUMMY_MESSEAGES.size();
    }

    public String getMessageBySeason(int month) {
        String season = getSeasonByMonth(month);
        List<String> message = DUMMY_MESSEAGES.get(season);
        return DUMMY_MESSEAGES.get(season).get(random.nextInt(message.size()));
    }

    private String getSeasonByMonth(int month) {
        if (month >= 3 && month <= 5) return "SPRING";
        if (month >= 6 && month <= 8) return "SUMMER";
        if (month >= 9 && month <= 11) return "AUTUMN";
        return "WINTER";
    }
